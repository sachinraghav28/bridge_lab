public class Averageofthreenumbers {
    public static void main(String[] args) {
        double num1 = 10.0; 
        double num2 = 20.0; 
        double num3 = 30.0; 
        double average = (num1 + num2 + num3) / 3;
        System.out.println("The average of the three numbers is: " + average);
    }
    
}
